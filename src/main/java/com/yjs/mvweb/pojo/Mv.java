package com.yjs.mvweb.pojo;

public class Mv {
    private int uploadid;
    private String uploadname;
    private String uploadsrc;
    private String uploadpicsrc;

    public int getUploadid() {
        return uploadid;
    }

    public void setUploadid(int uploadid) {
        this.uploadid = uploadid;
    }

    public String getUploadname() {
        return uploadname;
    }

    public void setUploadname(String uploadname) {
        this.uploadname = uploadname;
    }

    public String getUploadsrc() {
        return uploadsrc;
    }

    public void setUploadsrc(String uploadsrc) {
        this.uploadsrc = uploadsrc;
    }

    public String getUploadpicsrc() {
        return uploadpicsrc;
    }

    public void setUploadpicsrc(String uploadpicsrc) {
        this.uploadpicsrc = uploadpicsrc;
    }
}
